const frutas = ["Laranja", "Maçã"]

// .push() -> Adicionar elemento no fim da array
// .unshift() -> Adicionar elemento no começo da array
frutas.unshift("Banana") 

// .pop() -> Remover o último elemento da array
// .shift() -> Remover o primeiro elemento da array
frutas.shift()

console.log(frutas)
